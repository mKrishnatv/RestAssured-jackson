
package entities.response;


public class Medium {

    private Long Height;
    private String Url;
    private Long Width;

    public Long getHeight() {
        return this.Height;
    }

    public void setHeight(Long height) {
        this.Height = height;
    }

    public String getUrl() {
        return this.Url;
    }

    public void setUrl(String url) {
        this.Url = url;
    }

    public Long getWidth() {
        return this.Width;
    }

    public void setWidth(Long width) {
        this.Width = width;
    }

}
